public class minutesInAYear {
	
	public static void main(String[] args) {
		int Minutes; //declares variable
		
		Minutes = 60 * 24 * 365; // 60 minutes per hour * 24 hours per day * 365 days
		// when calculating, days and hours cancel leaving you with seconds
		
		System.out.println("There are a total of " + Minutes + " minutes in one year."); //displays answer	
		
				
	}

}
